# Column conventions emitted by model.predict_fold (the new pipeline).
HOUR_COLS: list[str] = [f"h{h:02d}" for h in range(24)]          # actuals
PRED_COLS: list[str] = [f"pred_h{h:02d}" for h in range(24)]     # predictions
REGIME_COL: str = "effective_regime"                             # 0=A, 1=B, 2=C0, 3=C1

# For readability when reading the printed "Cluster N" rows.
REGIME_LABELS: Dict[int, str] = {0: "A (negative)", 1: "B (spike)", 2: "C0", 3: "C1"}



# ##############################################################################
# WIDE  ->  LONG
# ##############################################################################

def to_long_predictions(
    df_preds: pd.DataFrame,
    zone_cfg: ZoneConfig,
    te_start: Optional[str] = None,
    te_end: Optional[str] = None,
) -> pd.DataFrame:
    """Reshape the new pipeline's wide daily predictions into one row per hour.

    Builds a tz-naive UTC hourly timestamp from each day's `date` plus the hour
    index parsed from the h00..h23 / pred_h00..pred_h23 columns. UTC hours match
    the old diagnostics, which also treated h00..h23 as UTC.

    Args:
        df_preds: Output of `load_or_train` / `walk_forward`. Must contain `date`,
            `h00..h23`, `pred_h00..pred_h23`, and `effective_regime`.
        zone_cfg: Zone configuration; only `zone_cfg.zone` is used (the `zone`
            column label so the Part 2 plots colour the series correctly).
        te_start: Optional inclusive lower bound on the day, e.g. "2025-06-18".
        te_end: Optional inclusive upper bound on the day, e.g. "2025-11-26".

    Returns:
        Long DataFrame sorted by time with columns:
            date_time | zone | actual | predicted | cluster
        where `cluster` carries `effective_regime` as an int .
    """
    df = df_preds.copy()

    # Normalise `date` to a tz-naive day, regardless of whether it arrives
    # tz-aware (df_daily is UTC-indexed) or already naive.
    day = pd.to_datetime(df["date"])
    if getattr(day.dt, "tz", None) is not None:
        day = day.dt.tz_convert("UTC").dt.tz_localize(None)
    df["_day"] = day.dt.normalize()

    # Restrict to the matched comparison window (inclusive on both ends, so the
    # final day's full 24 hours are kept - matching the old test set).
    if te_start is not None:
        df = df[df["_day"] >= pd.Timestamp(te_start)]
    if te_end is not None:
        df = df[df["_day"] <= pd.Timestamp(te_end)]

    if df.empty:
        raise ValueError(
            f"No predictions in window [{te_start}, {te_end}] for {zone_cfg.zone}. "
            f"Check that df_preds covers this fold."
        )

    regime = (
        df[REGIME_COL].astype("Int64")
        if REGIME_COL in df.columns
        else pd.Series(pd.NA, index=df.index, dtype="Int64")
    )
    base = pd.DatetimeIndex(df["_day"]).tz_localize("UTC")

    frames = []
    for h in range(24):
        ts = (base + pd.Timedelta(hours=h)).tz_localize(None)  # naive UTC instant
        frames.append(
            pd.DataFrame(
                {
                    "date_time": ts.to_numpy(),
                    "zone": zone_cfg.zone,
                    "actual": df[f"h{h:02d}"].to_numpy(dtype=float),
                    "predicted": df[f"pred_h{h:02d}"].to_numpy(dtype=float),
                    "cluster": regime.to_numpy(),
                }
            )
        )

    long_df = (
        pd.concat(frames, ignore_index=True)
        .sort_values("date_time")
        .reset_index(drop=True)
    )
    return long_df




